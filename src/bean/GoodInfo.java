package bean;

public class GoodInfo {
	private int gID;
	private String gInfo;
	private String gImgs;
	private int gNice;
	private int gCollect;
	private int gView;
	
	public GoodInfo(){
		
	}
	
	public int getgID() {
		return gID;
	}

	public void setgID(int gID) {
		this.gID = gID;
	}

	public String getgInfo() {
		return gInfo;
	}

	public void setgInfo(String gInfo) {
		this.gInfo = gInfo;
	}

	public String getgImgs() {
		return gImgs;
	}

	public void setgImgs(String gImgs) {
		this.gImgs = gImgs;
	}

	public int getgNice() {
		return gNice;
	}

	public void setgNice(int gNice) {
		this.gNice = gNice;
	}

	public int getgCollect() {
		return gCollect;
	}

	public void setgCollect(int gCollect) {
		this.gCollect = gCollect;
	}

	public int getgView() {
		return gView;
	}

	public void setgView(int gView) {
		this.gView = gView;
	}


}
